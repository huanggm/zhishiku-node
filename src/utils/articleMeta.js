const getLabelByValue = (value, arr) => {
  const found = arr.find(obj => obj.value === value)
  return found ? found.label : ''
}

const getGender = value => getLabelByValue(value, gender)
const getBirthday = value => value + '年'
const getMarriage = value => getLabelByValue(value, marriage)
const getMarriageSup = value => getLabelByValue(value, marriage_sup)
const getMarriageSub = value => getLabelByValue(value, marriage_sub)
const getEducation = value => getLabelByValue(value, education)
const getHouse = value => getLabelByValue(value, house)
const getHouseSup = value => getLabelByValue(value, house_sup)
const getHouseSub = value => getLabelByValue(value, house_sub)
const getCity = value => {
  const province = value.slice(0, 2) + '0000000000'
  const foundProvince = city.find(obj => obj.value === province)
  const foundCity = getLabelByValue(value, foundProvince.children)
  return foundProvince.label + foundCity
}
const getAge = value => new Date().getFullYear() - value

